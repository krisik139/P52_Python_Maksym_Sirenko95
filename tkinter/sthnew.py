import re
# \w - буква, цифра, _
# \W - все що не \w ,  # , ? , ,
# \d - цифра, 0-9
# \D - не цифра
# \s - Пробіл, таб
# \S - Не пробіл

test_patter = re.compile(r"\w\w\d")
# ab5, A_5, Xx9


# [a-z] - маленькі букви
# [A-Z] - тільки великі
# [0-9]
# [a-zA-Z0-9_] - всі символи логіна

# комбінування
# [abcXYZ] - тільки 6 символів
# [а-яА-Я] - тільки кирилиця

# Інверсія
# [^0-9] - будь що крім цифр
# [^a-zA-Z] - будь що крім букв


# Квантіфікатори - скільки символів потрібно в рядку
# * - 0 або більше - a* -> "" , a, aaa, aaaa
# + - 1 або більше - a+ - > a, aa, aaa,aaaa
# ? - 0 або 1  a? -> a , ""
# {n} рівно n разів  a{3} - > aaa
# {n,} мінімум n a{2,} -> aa, aaaa,aaaaaaa
# {n,m} від n до m   a{2,4} -> aa, aaa, aaaa
test_2_pattern = re.compile(r"^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]\.{6-20}$")

test_text = input("Довільний текст?")

if test_2_pattern.search(test_text):
    print("Да, довільний.")
else:
    print("Щось не довільний... Ау... АУАУ, ПФФФФФФФФФФФ")